				<h2>Riddiford St</h2>
				<p> Newtown's name "new town" suggests its earlier purposes, being one of Wellington's first suburbs. It has been through a colourful history since the 1840s. At its centre lies Riddiford St, named after Daniel Riddiford  (1814-75), one of Wellington's early settlers who became a wealthy landowner. Since the very start, Riddiford St has played a very important role in Newtown, with the first horse-drawn tram service established in the mid 1800s, allowing transport connections from the farer suburbs to the city centre. The street has held many important buildings, including the Newtown Library from 1902 to 1957, Wellington Hospital (built in 1881) and a theatre and cinema. Riddiford St also hosts the Newtown street festival and this has been going for over 20 years.</p>
