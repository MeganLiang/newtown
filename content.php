				<h2>In the present</h2>
				<p> Newtown in its present has evolved significantly, becoming as vibrant as ever, including many festivals and arts events. BMD (street artist) has even targeted the artistic suburb's no-longer-slabs-of-grey walls. There are some amazing cafes and art created which define Newtown's culture. The historic buildings that Newtown has are the backbone for the bursts of creativity that Newtown expels. Many of them can be viewed along Riddiford Street With it comes the legendary annual Newtown Festival and the Newtown Saturday Flea Market.</p>
